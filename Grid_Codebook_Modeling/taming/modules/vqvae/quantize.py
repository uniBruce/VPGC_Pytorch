class GridQuantizer(nn.Module):
    """
    Improved version over VectorQuantizer, can be used as a drop-in replacement. Mostly
    avoids costly matrix multiplications and allows for post-hoc remapping of indices.
    """
    # NOTE: due to a bug the beta term was applied to the wrong term. for
    # backwards compatibility we use the buggy version by default, but you can
    # specify legacy=False to fix it.
    def __init__(self, n_e, e_dim, embedding=None, beta=0.25, remap=None, unknown_index="random",
                 sane_index_shape=False, legacy=True, use_orthgonal=False):
        super().__init__()
        self.n_e = n_e
        self.e_dim = e_dim
        self.z_size = 16
        self.c_size = 16
        self.w_size = 16
        self.group = int(256/self.e_dim)
        self.depth = int(1024/self.e_dim)
        self.soft_quant = False
        self.beta = beta
        self.legacy = legacy
        self.use_orthgonal = use_orthgonal
        self.softmax = nn.Softmax(dim=-1)

        ##################### Defining Grid Codebook #######################
        # self.embedding = nn.Embedding(self.n_e, self.e_dim)
        # self.embedding.weight.data.uniform_(-1.0 / self.n_e, 1.0 / self.n_e) #[B, 16*16, 256] -> [B, 16*16, 4, 64]
        if embedding == None:
            self.embedding = nn.Parameter(torch.randn(self.n_e, self.depth, self.e_dim)) # [16*16， 16， 64] window [16*16, 64]
        else:
            self.embedding = embedding

        ################ Preparing Window Index in advance #################
        z_coord = self.mesh_grid(self.z_size)
        c_coord = self.mesh_grid(self.c_size)
        knn_dist, knn_idx = self.get_index(z_coord, c_coord, self.w_size)
        knn_idx = torch.from_numpy(np.array(knn_idx)) # [256, 16] K: window size
        print('Window Index for %d size Prepared!'%self.w_size)

        self.c_window = self.gather_data(self.embedding.data, knn_idx).view([self.n_e, -1, self.e_dim]) # [256, 16, 16, 64] -> [256, 16*16, 64]
        self.c_window = self.c_window.unsqueeze(0).repeat(4, 1, 1, 1) # [4, 256, 256, 64] 
        print('Window codebook for %d size Prepared!'%self.w_size)
        
        self.remap = remap
        if self.remap is not None:
            self.register_buffer("used", torch.tensor(np.load(self.remap)))
            self.re_embed = self.used.shape[0]
            self.unknown_index = unknown_index # "random" or "extra" or integer
            if self.unknown_index == "extra":
                self.unknown_index = self.re_embed
                self.re_embed = self.re_embed+1
            print(f"Remapping {self.n_e} indices to {self.re_embed} indices. "
                  f"Using {self.unknown_index} for unknown indices.")
        else:
            self.re_embed = n_e

        self.sane_index_shape = sane_index_shape

    def remap_to_used(self, inds):
        ishape = inds.shape
        assert len(ishape)>1
        inds = inds.reshape(ishape[0],-1)
        used = self.used.to(inds)
        match = (inds[:,:,None]==used[None,None,...]).long()
        new = match.argmax(-1)
        unknown = match.sum(2)<1
        if self.unknown_index == "random":
            new[unknown]=torch.randint(0,self.re_embed,size=new[unknown].shape).to(device=new.device)
        else:
            new[unknown] = self.unknown_index
        return new.reshape(ishape)

    def unmap_to_all(self, inds):
        ishape = inds.shape
        assert len(ishape)>1
        inds = inds.reshape(ishape[0],-1)
        used = self.used.to(inds)
        if self.re_embed > self.used.shape[0]: # extra token
            inds[inds>=self.used.shape[0]] = 0 # simply set to zero
        back=torch.gather(used[None,:][inds.shape[0]*[0],:], 1, inds)
        return back.reshape(ishape)

    def index_points(self, points, idx):
        """
        Input:
            points: input points data, [B, N, C]
            idx: sample index data, [B, S]
        Return:
            new_points:, indexed points data, [B, S, C]
        """
        device = points.device
        B = points.shape[0]
        view_shape = list(idx.shape)
        view_shape[1:] = [1] * (len(view_shape) - 1)
        repeat_shape = list(idx.shape)
        repeat_shape[0] = 1
        batch_indices = torch.arange(B, dtype=torch.long).to(device).view(view_shape).repeat(repeat_shape)
        new_points = points[batch_indices, idx, :]
        return new_points

    def chamfer_compare(self, z):
        '''
        z: input feature map [4, 256, 4, 64]
        c: codebook [4, 256, 256, 64]
        '''

        z = z.unsqueeze(3).repeat(1, 1, 1, self.depth*self.w_size, 1)
        c = self.c_window.unsqueeze(2).repeat(1, 1, self.group, 1, 1)     
        dist = torch.sum((z - c)**2, -1)
        min_encoding_indices = torch.argmin(dist, dim=3) # [4, 256, 4]

        idx = min_encoding_indices.view(-1, self.group)
        c_query = self.c_window.view(-1, self.depth*self.w_size, self.e_dim)
        #print(idx.shape, c_query.shape)
        code = self.index_points(c_query, idx)
        
        return min_encoding_indices, code

    def attention_aggregate(self, z):
        '''
        z: input feature map [B, N, C]
        c: codebook [M, C]
        '''
        B, N, C = z.shape
        z = z.unsqueeze(2).repeat(1, 1, self.w_size, 1).view(-1, self.w_size, C) # [BN, K, C]
        c_query = self.c_window.view(-1, self.w_size, C)
        device = z.device
        c_query = c_query.to(device)
        code = self.cross_attention(z, c_query)
        min_encoding_indices = None
        # dist = torch.sum((z - c_query**2), -1)
        # min_encoding_indices = torch.argmin(dist, dim=-2)
            
        return min_encoding_indices, code

    def cross_attention(self, z, c):
        '''
        z: input featuremap
        c: codebook
        '''
        print(z.shape, c.shape)
        
        dots = torch.matmul(z, c.transpose(-1, -2))
        dots = self.softmax(dots)
        out = torch.matmul(dots, c)
        # print(out.shape, out.device)
        out = torch.sum(out, dim=1)
        return out

    def gather_index(self, x, idx):
        '''
        x: [B, N, K, C]
        idx: [B, N]
        '''
        B, N, K, C = x.shape
        # y_list = []
        # for i in range(idx.shape[0]):
        #     for j in range(idx.shape[1]):
        #         y_list.append(x[i, j, idx[i, j], :])
        # out = torch.stack(y_list).view([B, N, C])

        out = torch.zeros([B, N, C])
        for i in range(idx.shape[0]):
            for j in range(idx.shape[1]):
                out[i, j, :] = x[i, j, idx[i, j], :]
        return out.cuda()

    def quant(self, z):
        self.c_window = self.c_window.to(z.device) # [B, N, K, C]
        z_batch = rearrange(z, 'b c h w -> b (h w) c').contiguous()
        z_batch = rearrange(z_batch, 'b n (k d) -> b n k d', k=self.group).contiguous()
        z = rearrange(z, 'b c h w -> b h w c').contiguous()

        if not self.soft_quant:
            min_encoding_indices, code = self.chamfer_compare(z_batch)
            z_q = code.view(z.shape)
        else:
            min_encoding_indices, code = self.attention_aggregate(z_batch)
            z_q = code.view(z.shape)

        z_q = rearrange(z_q, 'b h w c -> b c h w').contiguous()
        return z_q


    def forward(self, z, temp=None, rescale_logits=False, return_logits=False):
        assert temp is None or temp==1.0, "Only for interface compatible with Gumbel"
        assert rescale_logits==False, "Only for interface compatible with Gumbel"
        assert return_logits==False, "Only for interface compatible with Gumbel"
        # reshape z -> (batch, height, width, channel) and flatten
        self.c_window = self.c_window.to(z.device) # [B, N, K, C]
        z_batch = rearrange(z, 'b c h w -> b (h w) c').contiguous()
        z_batch = rearrange(z_batch, 'b n (k d) -> b n k d', k=self.group).contiguous()
        z = rearrange(z, 'b c h w -> b h w c').contiguous()
        #z_flattened = z.view(-1, self.e_dim)
        # distances from z to embeddings e_j (z - e)^2 = z^2 + e^2 - 2 e * z

        if not self.soft_quant:
            min_encoding_indices, code = self.chamfer_compare(z_batch)
            z_q = code.view(z.shape)
        else:
            min_encoding_indices, code = self.attention_aggregate(z_batch)
            z_q = code.view(z.shape)

        perplexity = None
        min_encodings = None

        # compute loss for embedding
        if not self.legacy:
            loss = self.beta * torch.mean((z_q.detach()-z)**2) + \
                   torch.mean((z_q - z.detach()) ** 2)
        else:
            loss = torch.mean((z_q.detach()-z)**2) + self.beta * \
                   torch.mean((z_q - z.detach()) ** 2)

        if self.use_orthgonal:
            loss += 10*orthgonal_loss_fn(self.embedding.weight)

        # preserve gradients
        z_q = z + (z_q - z).detach()

        # reshape back to match original input shape
        z_q = rearrange(z_q, 'b h w c -> b c h w').contiguous()

        if self.remap is not None:
            min_encoding_indices = min_encoding_indices.reshape(z.shape[0],-1) # add batch axis
            min_encoding_indices = self.remap_to_used(min_encoding_indices)
            min_encoding_indices = min_encoding_indices.reshape(-1,1) # flatten

        if self.sane_index_shape:
            min_encoding_indices = min_encoding_indices.reshape(
                z_q.shape[0], z_q.shape[2], z_q.shape[3])

        return z_q, loss, (perplexity, min_encodings, min_encoding_indices)

    def get_codebook_entry(self, indices, shape):
        # shape specifying (batch, height, width, channel)
        if self.remap is not None:
            indices = indices.reshape(shape[0],-1) # add batch axis
            indices = self.unmap_to_all(indices)
            indices = indices.reshape(-1) # flatten again

        # get quantized latent vectors
        z_q = self.embedding(indices)

        if shape is not None:
            z_q = z_q.view(shape)
            # reshape back to match original input shape
            z_q = z_q.permute(0, 3, 1, 2).contiguous()

        return z_q

    def linear_space(self, num):
        internal = 1.0 / num
        start = internal/2.0
        x_list = [start]
        for i in range(1, num):
            x_list.append(start + i*internal)
        return np.array(x_list)

    def mesh_grid(self, num):
        x = self.linear_space(num)
        y = self.linear_space(num)
        u,v = np.meshgrid(x, y) # [16, 16]
        u = np.expand_dims(u, 2)
        v = np.expand_dims(v, 2)
        uv = np.concatenate([u,v], -1)
        return uv

    def get_knn_idx(self, queries, pc, k):
        """
        queries [M, C]
        pc [P, C]
        """
        knn_search = NearestNeighbors(n_neighbors=k, algorithm='auto')
        knn_search.fit(pc)
        knn_dist, knn_idx = knn_search.kneighbors(queries, return_distance=True)
        return knn_dist, knn_idx

    def get_index(self, x, y, num):
        '''
        x: input coordinate
        y: codebook coordinate
        '''
        x = np.reshape(x, [-1, 2])
        y = np.reshape(y, [-1, 2])
        knn_dist, knn_idx = self.get_knn_idx(x, y, num)

        # x_tile = np.tile(np.expand_dims(x, 1), [1, y.shape[0], 1])
        # y_tile = np.tile(np.expand_dims(y, 0), [x.shape[0],1, 1])
        # dist = np.sqrt(np.sum((x_tile - y_tile)**2), -1)
        return knn_dist, knn_idx

    def gather_data(self, x, idx):
        '''
        x: flattened codebook [256, 16, 64]
        idx: [256, 16] N:256, K:16(neighbours)
        '''
        y_list = []
        for i in range(idx.shape[0]):
            y_list.append(x[idx[i], :, :])
        out = torch.stack(y_list, 0)
        return out


